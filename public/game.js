const startGame = function () {
    console.log("I'm starting a game now...");
    const gameBoard = document.querySelector("#gameArea");
    const pkmn = selectXPkmn(12);
    console.log(pkmn);
    let numSelect = numList.slice();
    for (let i = 0; i < gameBoard.clientHeight / 100; i++) {
        for (let i2 = 0; i2 < gameBoard.clientWidth / 100; i2++) {
            console.log("Making some cards!");
            const box = document.createElement("div");
            const img = document.createElement("img");
            const back = document.createElement("img");
            img.classList.add("cardImgHidden");
            img.classList.add("cardImg");
            //back.src = "./public/Pokemon Image/pokeball.png"
            //back.classList.add("cardImg");
            //back.classList.add("cardBackVisible")
            //back.id = "back";
            box.classList.add("cards");
            //box.classList.add(".flip-card") //test
            box.addEventListener("click", cardClickFlip);
            let pkmnChoice;
            pkmnChoice = numSelect.splice(
                Math.floor(Math.random() * numSelect.length),
                1
            )[0];
            if (pkmnChoice === 12) {
                img.src = "./public/Pokemon Image/pokeball.png";
            } else {
                img.src = `./public/Pokemon Image/${pkmn[pkmnChoice]}`;
            }
            gameBoard.appendChild(box);
            //box.appendChild(back);
            box.appendChild(img);
        }
    }
    this.removeEventListener("click", startGame);
};

const cardClickFlip = function () {
    const img = this.querySelector(".cardImg");
    //const back = this.querySelector("#back");
    const allC = document.querySelectorAll(".cardImg").length;
    const allH = document.querySelectorAll(".cardImgHidden").length;
    const allS = document.querySelectorAll(".cardSolved").length;
    const i = allC - allH - allS;
    let flippedCards = document.querySelectorAll(".cardImg");
    let newCard;
    let newImg;
    if (i >= 2) {
        console.log("Too many cards flipped, hidding them");
        for (card of flippedCards) {
            if (card.parentNode.classList.contains("cardSolved")) {
                continue;
            }
            card.classList.add("cardImgHidden");
            //back.classList.remove("cardImgHidden");
        }
    }
    flippedCards = [];
    for (card of document.querySelectorAll(".cardImg")) {
        if (!card.classList.contains("cardImgHidden")) {
            if (!card.parentNode.classList.contains("cardSolved")) {
                newCard = card;
                newImg = card.src;
            }
            flippedCards.push(card);
            if ((newImg == img.src) & (newCard !== img)) {
                console.log("SOLVED!!!");
                img.parentNode.classList.add("cardSolved");
                newCard.parentNode.classList.add("cardSolved");
                img.parentNode.removeEventListener("click", cardClickFlip);
                newCard.parentNode.removeEventListener("click", cardClickFlip);
            }
        }
    }
    if (flippedCards.length === 24) {
        document.querySelector("#start").addEventListener("click", startGame);
        alert("You've won!!!");
        for (card of document.querySelectorAll(".cards")) {
            card.parentNode.removeChild(card);
        }
        //for (back of document.querySelectorAll("#back")) {
        //    back.parentNode.removeChild(back);
        //}
    }
    if (img.classList.contains("cardImgHidden")) {
        img.classList.remove("cardImgHidden");
        //back.classList.add("cardImgHidden");
    } else {
        img.classList.add("cardImgHidden");
        //back.classList.remove("cardImgHidden");
    }
};

